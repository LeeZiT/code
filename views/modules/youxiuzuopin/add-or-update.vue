				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					jingsaimingcheng : false,
					zuopinmingcheng : false,
					fengmian : false,
					zuopinwenjian : false,
					zuopinjianjie : false,
					zuopinxiangqing : false,
					xuehao : false,
					xingming : false,
					gonghao : false,
					fabushijian : false,
					crossuserid : false,
					crossrefid : false,
					thumbsupnum : false,
